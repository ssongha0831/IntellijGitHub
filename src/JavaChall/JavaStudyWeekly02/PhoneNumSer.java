package JavaChall.JavaStudyWeekly02;

public class PhoneNumSer {
    private String name;
    private String phoneNum;

    public PhoneNumSer(String name, String phoneNum) {
        super();
        this.name = name;
        this.phoneNum = phoneNum;
    }

    public String getName() {
        return name;
    }

    public String getPhoneNum() {
        return phoneNum;
    }

}
