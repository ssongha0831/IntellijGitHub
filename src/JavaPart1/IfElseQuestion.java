package JavaPart1;

public class IfElseQuestion {

    public static void main(String[] args) {
        int number = 5;
        int result = ( number % 2 );

        if (result != 2 ) {
            System.out.println("홀수 입니다.");
        } else {
            System.out.println("짝수 입니다.");
        }
    }
}
